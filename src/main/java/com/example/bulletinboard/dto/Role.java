package com.example.bulletinboard.dto;

/**
 * Перечисление Role представляет роли пользователей в приложении.
 * Роли могут быть различными, например, "USER" и "ADMIN".
 */
public enum Role {

    /**
     * Роль пользователя.
     */
    USER,

    /**
     * Роль администратора.
     */
    ADMIN
}